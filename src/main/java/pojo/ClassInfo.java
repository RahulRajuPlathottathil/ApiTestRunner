package pojo;

import java.util.List;

/**
 * @author Lenovo
 *
 */
public class ClassInfo {
	private int adultsCount;
	private List<String> kidsAges;
	/**
	 * @return the adultsCount
	 */
	public int getAdultsCount() {
		return adultsCount;
	}
	/**
	 * @param adultsCount the adultsCount to set
	 */
	public void setAdultsCount(int adultsCount) {
		this.adultsCount = adultsCount;
	}
	/**
	 * @return the kidsAges
	 */
	public List<String> getKidsAges() {
		return kidsAges;
	}
	/**
	 * @param kidsAges the kidsAges to set
	 */
	public void setKidsAges(List<String> kidsAges) {
		this.kidsAges = kidsAges;
	}
	

}
